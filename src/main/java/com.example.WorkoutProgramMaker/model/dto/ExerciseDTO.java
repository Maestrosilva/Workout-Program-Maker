package com.example.WorkoutProgramMaker.model.dto;

import com.example.WorkoutProgramMaker.model.entity.enums.ExerciseType;

import java.time.DayOfWeek;

public class ExerciseDTO {
    private ExerciseType exerciseType;
    private Integer numberOfSets;
    private Double kg;
    private Integer reps;
    private DayOfWeek workoutDay;
    private Integer number;

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public ExerciseDTO() {
    }

    public ExerciseType getExerciseType() {
        return exerciseType;
    }

    public void setExerciseType(ExerciseType exerciseType) {
        this.exerciseType = exerciseType;
    }

    public Integer getNumberOfSets() {
        return numberOfSets;
    }

    public void setNumberOfSets(Integer numberOfSets) {
        this.numberOfSets = numberOfSets;
    }

    public Double getKg() {
        return kg;
    }

    public void setKg(Double kg) {
        this.kg = kg;
    }

    public Integer getReps() {
        return reps;
    }

    public void setReps(Integer reps) {
        this.reps = reps;
    }

    public DayOfWeek getWorkoutDay() {
        return workoutDay;
    }

    public void setWorkoutDay(DayOfWeek workoutDay) {
        this.workoutDay = workoutDay;
    }
}
